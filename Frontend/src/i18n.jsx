const LanguageContext = createContext({
  lang: "en",
  setLang: () => {},
  t: (key) => key,
});

const resolveKey = (obj, key) => {
  return key.split(".").reduce((acc, part) => {
    if (!acc) return undefined;
    return acc[part];
  }, obj);
};

const interpolate = (str, params) =>
  str.replace(/\{(\w+)\}/g, (_, k) => (params[k] !== undefined ? params[k] : `{${k}}`));

export const LanguageProvider = ({ children }) => {
  const [lang, setLang] = useState(() => {
    if (typeof window === "undefined") return "en";
    return localStorage.getItem("lang") || "en";
  });

  useEffect(() => {
    if (typeof window === "undefined") return;
    localStorage.setItem("lang", lang);
  }, [lang]);

  const t = (key, params = {}) => {
    const value = resolveKey(translations[lang], key);
    if (!value) return key;
    if (typeof value === "string") return interpolate(value, params);
    if (typeof value === "function") return value(params);
    return value;
  };

  const ctxValue = useMemo(() => ({ lang, setLang, t }), [lang]);

  return <LanguageContext.Provider value={ctxValue}>{children}</LanguageContext.Provider>;
};

export const useLanguage = () => useContext(LanguageContext);











